-- SHOW A REPORT OF THE LAST 3 PHONE NUMBERS ASSOCIATED WITH EACH PERSON IN TIME
;WITH RANKED_CONTACTS AS (
	SELECT PERSON_ID, CONTACT_VALUE, DENSE_RANK() OVER (PARTITION BY PERSON_ID, CONTACT_TYPE ORDER BY UPDATED_AT DESC) AS RANKID
	FROM @CONTACT_REFS
	WHERE CONTACT_TYPE='PHONE'
)
SELECT PERSON_ID, [1] AS PHONE1,[2] AS PHONE2, [3] AS PHONE3
FROM RANKED_CONTACTS
PIVOT (
	MIN(CONTACT_VALUE)
	FOR RANKID IN ([1], [2], [3])
) AS PVT
